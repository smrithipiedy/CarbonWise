import rateLimit from 'express-rate-limit';

/** Protects the insights endpoint from quota abuse. */
export const insightsRateLimiter = rateLimit({
  windowMs: 15 * 60 * 1000,
  max: 30,
  standardHeaders: true,
  legacyHeaders: false,
  message: { error: 'Too many insight requests. Please try again later.' },
});

/** General API rate limit for write operations. */
export const apiRateLimiter = rateLimit({
  windowMs: 15 * 60 * 1000,
  max: 100,
  standardHeaders: true,
  legacyHeaders: false,
  message: { error: 'Too many requests. Please try again later.' },
});
